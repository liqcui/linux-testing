    echo ""
    echo "场景3: 双主机SSH迁移"
    echo "----------------------------------------"
    echo ""
    echo "准备工作:"
    echo "  1. 配置SSH密钥认证"
    echo "     ssh-keygen"
    echo "     ssh-copy-id root@target-host"
    echo ""
    echo "  2. 确保共享存储或使用存储迁移"
    echo ""
    echo "执行迁移:"
    echo "  virsh migrate --live \\"
    echo "      --verbose \\"
    echo "      test-migrate \\"
    echo "      qemu+ssh://target-host/system"
    echo ""
    echo "监控进度:"
    echo "  watch -n 1 'virsh domjobinfo test-migrate'"
